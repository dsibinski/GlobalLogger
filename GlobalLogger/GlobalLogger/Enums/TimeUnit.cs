        /// Days
        /// </summary>
        Days,
        /// <summary>
        /// Hours
        /// </summary>
        Hours,
        /// <summary>
        /// Minutes
        /// </summary>
        Minutes,
        /// <summary>
        /// Seconds
        /// </summary>
        Seconds,
        /// <summary>
        /// Miliseconds
        /// </summary>
        Miliseconds,
        /// <summary>
        /// Processor ticks
        /// </summary>
        Ticks
    }
}
